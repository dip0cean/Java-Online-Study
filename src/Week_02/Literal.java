package Week_02;

public class Literal {
    public static void main(String[] args) {
        // 2진수
        int lit2 = 0b11010;
        System.out.println(lit2);

        // 8진수
        int lit8 = 013;
        System.out.println(lit8);

        // 16진수
        int lit16 = 0xB3;
        System.out.println(lit16);
    }
}
